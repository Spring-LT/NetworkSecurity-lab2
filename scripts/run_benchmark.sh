#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT_DIR"

TARGET_MIB="${BENCH_TARGET_MIB:-1}"
MIN_ITERS="${BENCH_MIN_ITERS:-8}"
CXX="${CXX:-g++}"

mkdir -p bin report benchmark scripts

echo "[INFO] Building benchmark with -O3 -DNDEBUG ..."
"$CXX" -std=c++17 -O3 -DNDEBUG -Iinclude \
    -DBENCH_TARGET_MIB="$TARGET_MIB" \
    -DBENCH_MIN_ITERS="$MIN_ITERS" \
    benchmark/benchmark_crypto.cpp \
    src/des.cpp src/tdes.cpp src/aes.cpp src/utils.cpp \
    -o bin/benchmark_crypto

echo "[INFO] Running benchmark ..."
./bin/benchmark_crypto

echo "[INFO] Generating Markdown and LaTeX tables ..."
python3 - <<'PY'
import csv
from pathlib import Path

csv_path = Path("report/benchmark_results.csv")
md_path = Path("report/benchmark_results.md")
tex_path = Path("report/benchmark_results_table.tex")

rows = list(csv.DictReader(csv_path.open("r", encoding="utf-8")))

with md_path.open("w", encoding="utf-8") as f:
    f.write("# Crypto Benchmark Results\n\n")
    f.write("| Algorithm | Message size (B) | Iterations | Encrypt latency (us/op) | Decrypt latency (us/op) | Encrypt throughput (MiB/s) | Decrypt throughput (MiB/s) | Round-trip throughput (MiB/s) |\n")
    f.write("|---|---:|---:|---:|---:|---:|---:|---:|\n")
    for r in rows:
        f.write(
            f"|{r['algorithm']}|{r['message_size']}|{r['iterations']}|"
            f"{float(r['enc_us_per_op']):.2f}|{float(r['dec_us_per_op']):.2f}|"
            f"{float(r['enc_mib_s']):.2f}|{float(r['dec_mib_s']):.2f}|"
            f"{float(r['roundtrip_mib_s']):.2f}|\n"
        )

with tex_path.open("w", encoding="utf-8") as f:
    f.write("% Auto-generated by scripts/run_benchmark.sh\n")
    f.write("\\begin{table}[H]\n")
    f.write("    \\centering\n")
    f.write("    \\caption{DES / 3DES / AES-128 定量性能测试结果（\\texttt{-O3 -DNDEBUG}）}\n")
    f.write("    \\label{tab:alg-performance-compare-quant}\n")
    f.write("    \\renewcommand{\\arraystretch}{1.25}\n")
    f.write("    \\begin{tabularx}{\\textwidth}{>{\\centering\\arraybackslash}p{1.7cm} >{\\centering\\arraybackslash}p{1.8cm} >{\\centering\\arraybackslash}p{1.8cm} >{\\centering\\arraybackslash}p{2.0cm} >{\\centering\\arraybackslash}p{2.0cm} Y}\n")
    f.write("        \\toprule\n")
    f.write("        \\textbf{算法} & \\textbf{消息长度(B)} & \\textbf{迭代次数} & \\textbf{加密时延(\\textmu s/次)} & \\textbf{解密时延(\\textmu s/次)} & \\textbf{往返吞吐率(MiB/s)} \\\\\n")
    f.write("        \\midrule\n")
    for i, r in enumerate(rows):
        f.write(
            f"        {r['algorithm']} & {r['message_size']} & {r['iterations']} & "
            f"{float(r['enc_us_per_op']):.2f} & {float(r['dec_us_per_op']):.2f} & "
            f"{float(r['roundtrip_mib_s']):.2f} \\\\\n"
        )
        if i != len(rows) - 1:
            f.write("        \\midrule\n")
    f.write("        \\bottomrule\n")
    f.write("    \\end{tabularx}\n")
    f.write("\\end{table}\n")
PY

echo "[INFO] Benchmark finished."
echo "[INFO] CSV      : report/benchmark_results.csv"
echo "[INFO] Markdown : report/benchmark_results.md"
echo "[INFO] LaTeX    : report/benchmark_results_table.tex"
